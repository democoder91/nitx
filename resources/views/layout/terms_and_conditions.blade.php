                            ان مسؤولية ظهور اي محتوى من خلال منصة نتكس يعتبر مسؤولية نتكس وتتحمل نتكس ذلك 
                            
                        </p>
                        <h4><strong>عدم القابلية للتطبيق</strong></h4>
                        <p>
                            إذا ثبت أن أي من الاحكام الواردة هنا غير قابل للتطبيق بموجب القانون المطبق فإن ذلك لن يؤثر على قابلية الأحكام الأخرى للتطبيق.
                        </p>
