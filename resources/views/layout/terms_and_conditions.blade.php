                        <p>
                            إنك توافق على أن التعهد بعدم تحريك أي مطالبة قانونية شخصية ضد مسؤولي نتكس وموظفيها عن أي خسارة تكبدتها فيما يتعلق باستخدام الموقع
                            إنك توافق على أن حدود المسؤولية وتقييدها الوارد في هذه الاحكام و الشروط سوف تنطبق و تحمي مسؤولي نتكس وموظفيها ووكلائها وشركاتها التابعة ومقاوليها ونتكس ذاتها
                        </p>
