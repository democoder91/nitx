                        أجل تسوية أي مطالبة أو نزاع وفقاً لرأي المستشارين القانونيين لنتكس) تتكبدها نتكس بسبب مخالفتك لهذه الأحكام والشروط أو أي جزء منها، أو تنشأ 
                        عن أي إدعاء بأنك خالفت مقتضياتها.
                        </p>
                        <h4><strong>مخالفة هذه الاحكام والشروط</strong></h4>
                        <p>
