                            لا يوجد شيء في هذه الاحكام والشروط من شأنه أن يستبعد أو يحدد أي ضمانات مقررة بموجب القانون الواجب التطبيق والتي يعتبر استبعادها أو تحديدها مخالفاً للقانون، كما أنه ليس هناك شيء فيها من شأنه أن يستبعد أو يحدد مسوؤلية نتكس فيما يتعلق بأي
                        <ul style="direction: rtl">
                            <li>وفاة أو ضرر شخصي نتج عن إهمال وتقصير نتكس.</li>
                            <li>الاحتيال أو الادعاءات الاحتيالية من قبل نتكس، أو</li>
                            <li>أي أمر يعتبر تحديد المسؤولية عنه أو استبعادها أو محاولة تحديدها أو استبعادها أو الايهام بتحديدها أو استبعادها من قبل نتكس مخالفاً للقانون أو غير مشروع.</li>
                        </ul>
                        </p> <h4><strong>المعقولية</strong></h4>
